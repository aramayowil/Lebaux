interface Ventanas {
  Medidas: string
  'DVH 4/9/4': string
  'DVH 3+3/9/4': string
  '3+3 LAM S': string
  'FLOAT 4MM': string
  MOSQUITERO: string
  PREMARCO: string
  'CONTRAMARCO SOLO': string
  field9: string
  'GUIA DE PERSIANA': string
  ID_ROW?: number
  AREA?: number
}

const AgregarAreaVentanas = (lista: Ventanas[]): Ventanas[] => {
  //bug si las mas medidas no tienen el formato correcto, se rompe el codigo
  return lista.map((item, index) => {
    const regex = /^\d+x\d+$/
    if (!regex.test(item.Medidas)) {
      return {
        ...item,
        AREA: 0,
        ID_ROW: index,
        field9: '',
        'GUIA DE PERSIANA': '',
        MOSQUITERO: '0',
        PREMARCO: '0',
        'CONTRAMARCO SOLO': '0',
        Medidas: '',
      } // Retorna un objeto con AREA 0 si el formato es incorrecto
    }

    const [ancho, alto] = item.Medidas.split('x').map(Number)
    const area = ancho * alto

    return {
      ...item,
      AREA: area,
      ID_ROW: index,
    }
  })
}

export const searchReference = (base: number, altura: number) => {
  // Buscar la referencia por nombre, base y altura
  let ventanas: Ventanas[] = []

  const ventanasLS = JSON.parse(localStorage.getItem('CorredizasLS') || '[]')

  if (Array.isArray(ventanasLS) && ventanasLS.length > 0) {
    ventanas = ventanasLS
  } else {
    ventanas = AgregarAreaVentanas(Corredizas)
    localStorage.setItem('CorredizasLS', JSON.stringify(ventanas))
  }
  const objetivo = base * altura

  const objetivoMasCercano = ventanas.reduce((prev, curr) => {
    const areaPrev = prev.AREA ?? 0
    const areaCurr = curr.AREA ?? 0

    return Math.abs(areaCurr - objetivo) < Math.abs(areaPrev - objetivo)
      ? curr
      : prev
  })
  return objetivoMasCercano
}

